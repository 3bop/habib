# addition # جمع

print(10 + 30) # 40
print(-10 + 20) # 10
print(1 + 2.66) # 3.66
print(1.2 +1.2) # 2.4

# Subtraction # طرح

print(60 - 30) # 30
print(-30 - 20) # -50
print(-30 - -20) # 10
print(5.66 - 3.44) # 2.22 

# Multiplication # ضرب

print(10 * 3) # 30
print(5 + 10 * 100) # 1005
print((5 + 10) * 100) #1500

# Divition # قسمه

print(100 / 20) # 5.0
print(int(100 / 20)) # 5

# Modulus # باقى القسمه 

print(8 % 2) # 0
print(9 % 2) # 1

# Exponent # اس

print(2 ** 5) # 32

# Floor Division # القسمه الى اعداد صحيحه

print(100 // 20) # 5
print(110 // 20) # 5
print(119 // 20) # 5
print(120 // 20) # 6
